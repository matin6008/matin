function getLang() {
  return localStorage.getItem('lang') === 'fa' ? 'fa' : 'en';
}

function t(key) {
  const lang = getLang();
  return I18N[lang][key] ?? I18N.en[key] ?? key;
}

/* Picks the right language field from a product row, e.g. lf(p, 'name') → name_fa or name_en. */
function lf(obj, field) {
  const v = obj[`${field}_${getLang()}`];
  return v || obj[`${field}_en`] || '';
}

function applyI18n(root = document) {
  const lang = getLang();
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'fa' ? 'rtl' : 'ltr';
  root.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll('[data-i18n-ph]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

function toggleLang() {
  localStorage.setItem('lang', getLang() === 'fa' ? 'en' : 'fa');
  applyI18n();
}
